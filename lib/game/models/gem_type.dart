/// The 6 gem types used on the Adinkra Gems board.
///
/// Each type maps to a specific Adinkra symbol and tile asset.
enum GemType {
  /// Red gem — Funtumfunefu Denkyemfunefu (unity in diversity)
  redFuntumfunefuDenkyemfunefu,

  /// Silver gem — Nsoromma (child of the heavens)
  silverNsoromma,

  /// Yellow gem — Sankofa (learn from the past)
  yellowSankofa,

  /// Purple gem — Akofena (courage and authority)
  purpleAkofena,

  /// Green gem — Gye Nyame (supremacy of God)
  greenGyeNyame,

  /// Blue gem — Dwennimmen (strength and humility)
  blueDwennimmen,
}
